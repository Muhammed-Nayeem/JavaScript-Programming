//How to use Maths object built in function

var x = Math.E;
var y = Math.PI;

var numOne = -4.568;
var numTwo = 4.568;

var num = 9;

console.log(x);
console.log(y);

console.log(Math.abs(numOne)); // It will return the absolute value from that negative floating value;
console.log(Math.floor(numTwo)); // It will return the min nearest integer value from that floating value;
console.log(Math.ceil(numTwo)); // It will return the nearest max nearest integer value from that value;
console.log(Math.round(numTwo)); // if value .5 than rounded up otherwise rounded down;
console.log(Math.max(400, 200, 600)); // return the max value;
console.log(Math.min(500, 300, 800)); // return the min value;
console.log(Math.pow(3, 2));
console.log(Math.pow(5, 2));
console.log(Math.sqrt(num));
console.log(Math.round(Math.random() * 50 + 1));