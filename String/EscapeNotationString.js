// How to use Escape Notation in javascript:

var str = 'This is a \'String\'';
var str2 = 'This is a another \"String\"';
var str3 = 'Here is a another \tString';
var str4 = 'Here Comes one more \\String';
var str5 = 'Here Comes one more \nString';
var str6 = 'Title A \fTitle B';
var str7 = 'To be \ror not';

console.log(str);
console.log(str2);
console.log(str3);
console.log(str4);
console.log(str5);
console.log(str6);
console.log(str7);