// What is an Object?
// -> Everything which takes multiple properties to describe is an object.

// Example 01: Person(A Men)
// Name, Status, Education, Address, Height, Body-Shape;


// Example 02: Books
// Book-Name, Author-Name, Publication-Name, Total-Page, Price;


// Example 03: Even a number can be Object:
// 450.0000 -> Integer part, Floating part, Negative, Positive, String value;